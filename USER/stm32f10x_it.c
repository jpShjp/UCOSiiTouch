/*******************************************************************************
* Function Name  : USART1_IRQHandler
* Description    : This function handles USART1 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void USART1_IRQHandler(void)
{
	OS_CPU_SR  cpu_sr;

	if(USART_GetITStatus(USART1,USART_IT_RXNE) != RESET)
	{
		BSP_Comm_Rx_Mode(1);
		
		OS_ENTER_CRITICAL();
		COMM1_ReceBuf.DataBuf[COMM1_ReceBuf.DataCount] = (u8)USART_ReceiveData(USART1);
		COMM1_ReceBuf.DataCount ++;
		COMM1_ReceBuf.DataLong ++;
		if(COMM1_ReceBuf.DataCount > 99)
		{
			COMM1_ReceBuf.DataCount = 0;
			COMM1_ReceBuf.DataLong = 0;
		}
		COMM1_ReceBuf.OK = 1;
		COMM1_ReceBuf.FrameDelayMS = 40;
		OS_EXIT_CRITICAL();
	}

	if(USART_GetITStatus(USART1,USART_IT_TC) != RESET)
	{
		OS_ENTER_CRITICAL();
		if(COMM1_SendBuf.DataCount >= COMM1_SendBuf.DataLong)
		{
			USART_ITConfig(USART1,USART_IT_TC,DISABLE);
			COMM1_SendBuf.OK = 0;
			BSP_Comm_Idle_Mode(1);
		}
		else
		{
			USART_SendData(USART1,(u16)COMM1_SendBuf.DataBuf[COMM1_SendBuf.DataCount]);
			COMM1_SendBuf.DataCount ++;
		}
		OS_EXIT_CRITICAL();
	}
	OSIntExit();
}

